public class DecimalVariablesType {
    public static void main(String[] args) {

        float number1= 34.5f; // you have to write "f,F" after the number to divide double variable
        float number2=35.1F;
        System.out.println(number2+number1);
        double a = 3.14;
        System.out.println(a);

    }

}
