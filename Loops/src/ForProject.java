public class ForProject {
    public static void main(String[] args){
        System.out.println("Start loops");
        for(int i=1; i<=5 ; i++){
            System.out.print(i);
        }
        System.out.println("\nStart loops");

        for(int k=1; k<=5 ; k++){
            for(int a=1; a<=5 ; a++){
                System.out.println("k="+ k + " a="+a);
            }
        }


    }
}
