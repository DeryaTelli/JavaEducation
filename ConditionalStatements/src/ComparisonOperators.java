public class ComparisonOperators {
        public static void main(String[]args){
            int a=10 ,b=5;
            boolean result = a==b;
            System.out.println(result);
            System.out.println(!result); // be true because (!) means not
            boolean result1= a!=b;
            System.out.println(result1);
            boolean result2= a>b;
            System.out.println(result2);



        }
}
