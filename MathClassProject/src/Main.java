public class Main {
    public static void main(String[] args) {
      System.out.println(Math.ceil(1.6)); // bir buyuk tam sayiya yuvarlama
      System.out.println(Math.cos(30));// cos degerini verir
      System.out.println(Math.floor(1.6));//bir kucuk tam sayiya yuvarlama
      System.out.println(Math.max(1.2 , 1));// buyuk sayiyi verir
      System.out.println(Math.pow(3,2));// 3^2 us alir
      System.out.println(Math.round(1.5));// yakin olan tam sayiya doner
      System.out.println(Math.abs(-1));// mutlak deger
      System.out.println(Math.sqrt(4));// karakok aliyor buda



    }
}